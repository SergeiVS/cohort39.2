package primitiv;

public class ArithmeticOperations2 {
    public static void main(String[] args) {

        int x = 10;
        int y = 5;

        System.out.println(x / y);

        x = 10;
        y = 3;

        System.out.println(x / y);
        System.out.println(x % y);

        double xDouble = 10.0;

        System.out.println(xDouble / y);

        System.out.println();

        double doubleResult = (double) x / y;

        System.out.println(doubleResult);


    }
}
